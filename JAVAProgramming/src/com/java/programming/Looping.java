package com.java.programming;

public class Looping {

	public static void main(String[] args) {
	
		//While 
		
		int i=1;
		while(i<=10) {
			System.out.println(i);
			i++; // i=i+1
		}
		int j=10;
		while(j>=1) {
			System.out.println(j);
			j--;//j=j-1
		}
		
		for(int a= 10;a>=1;a--) {
			System.out.println(a);
		}
	
		for(int b= 1;b<=10;b++) {
			System.out.println(b);
		}

	}

}
