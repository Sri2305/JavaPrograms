package com.java.programming;

public class FirstProgram_datatypes {

	public static void main(String[] args) {
	//datatypes
		//Integer no duplicate variables
		int a=10, j=20;
		a=j;j=a;
		System.out.println(a+j);
		
		//double decimal (can store integers in double)
		double b= 234.7654;
		double c= 10;
		System.out.println(c);
		
		//char - only single value
		char f='1';
		System.out.println(f);
		
		//boolean true or false (values and keywords)
		boolean a1= true;
		System.out.println(a1);
		
		//String - not a datatype and it is class
		String s="Alphan98767 ric !\"£$%";
		System.out.println(s);
	}

}
